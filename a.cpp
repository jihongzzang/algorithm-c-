/** 헤더파일을 include 시키다.*/
#include <bits/stdc++.h>

/** std라는 네임스페이스(namespace) 를 사용한다. */
using namespace std;

/** 문자열변수 a를 선언 */
string a;

int main() {
  /** 변수 a를 입력받다.대표적으로 입력함수로는 cin, scanf*/
  cin >> a;

  /** 변수 a를 출력한다.대표적으로 출력함수는 cout과 printf*/
  cout << a << "\n";

  /** main함수를 종료시키는 return 0입니다. */
  return 0;
}